package module02.task01_4.WhileCycle;

public class WhileCycle
{
    
    public static void main(String[] args) {
        int i=200000;
        while (i<=210000){
            System.out.println(i++);
        }
        i=220000;
        while (i<=235000)
        {
            System.out.println(i++);
        }
    }
}