package module02.task01_4.ForCycle;

public class ForCycle
{
    
    public static void main(String[] args) {
        
        for(int i=200000;i<=210000;i++)
        {
            System.out.println(i);
        }
        
        for(int i=220000;i<=235000;i++)
        {
            System.out.println(i);
        }
    }
}
